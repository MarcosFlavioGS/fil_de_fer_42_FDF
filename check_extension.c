int	check_file_extension(char *file)
{
	int	i;

	i = 0;
	while (file[i])
		i++;
	if (file[i - 1] == 'f' && file[i - 2] == 'd' && file[i - 3] == 'f'
		&& file[i - 4] == '.')
		return (1);
	return (0);
}
